    public void cargarMetodos(){
        ArrayList<String> metodos = new ArrayList<String>();        
        metodos.add("Transposicion por Columnas");
        metodos.add("Transposicion por Zig-Zag");
        //cargar los metodos de cifrado en combobox
        for(String metodo: metodos){
            this.jComboBox_metodos.addItem(metodo);
        }
    }

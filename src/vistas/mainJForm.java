    private void jButton_CifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CifrarActionPerformed
        String metodoSeleccionado = jComboBox_metodos.getSelectedItem().toString();
        String texto = jTextArea_texto.getText();
        int clave = Integer.valueOf(jTextField_clave.getText());
        switch(metodoSeleccionado){
            case "Transposicion por Columnas":{
                Columnas textoClaro = new Columnas();
                jTextArea_resultado.setText(textoClaro.cifrar(texto, clave));
                break;
            }
            case "Transposicion por Zig-Zag":{
                ZigZag textoClaro = new ZigZag();
                jTextArea_resultado.setText(textoClaro.cifrar(texto, clave));
                break;
            }
        }
    }//GEN-LAST:event_jButton_CifrarActionPerformed

